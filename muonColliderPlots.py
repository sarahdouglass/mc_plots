def main():
    import argparse
    parser = argparse.ArgumentParser()
    parser.add_argument("--filelist", type=str, default=None,
                        help="override filelist path (e.g. xrootd version for batch jobs)")
    args = parser.parse_args()

    cfg = CONFIG

    # allow overriding the filelist from the command line — used by run_analysis.sh
    # when submitting to OSG where filelist_xrootd.txt has xrootd-prefixed paths
    if args.filelist:
        cfg = dict(cfg)
        cfg["filelist"] = args.filelist

    # ── collect files ──────────────────────────────────────────────────────
    no_bib_files = sorted(glob.glob(cfg["no_bib_glob"])) if cfg["no_bib_glob"] else []

    if cfg.get("filelist"):
        with open(cfg["filelist"]) as f:
            bib_files = sorted([l.strip() for l in f if l.strip()])
    elif cfg["bib_glob"]:
        bib_files = sorted(glob.glob(cfg["bib_glob"]))
    else:
        bib_files = []

    if not no_bib_files and not bib_files:
        sys.exit("no files found. check filelist.txt or glob patterns in CONFIG.")

    print(f"No-BIB files : {len(no_bib_files)}")
    print(f"BIB files    : {len(bib_files)}")

    datasets = {}

    if no_bib_files:
        print("\nProcessing no-BIB files …")
        datasets["no_bib"] = process_files(no_bib_files, cfg).arrays()

    if bib_files:
        print("\nProcessing BIB files …")
        datasets["bib"] = process_files(bib_files, cfg).arrays()

    if len(datasets) == 1:
        print("\nSingle-dataset mode — all plots will show one dataset only.")

    # sanity check: print total truth muons, matched count, and overall efficiency
    for tag, arr in datasets.items():
        n  = len(arr["all_true_pt"])
        nm = arr["all_matched"].sum()
        print(f"\n[{STYLE[tag]['label']}]  truth muons: {n}  |  matched: {nm}  |  eff: {nm/n*100:.1f}%")

    # ── generate plots ─────────────────────────────────────────────────────
    bins = cfg["pt_bins"]
    print("\nGenerating plots …")
    plot_residual_distributions(datasets, cfg)
    plot_pt_resolution(datasets, bins, cfg)
    plot_efficiency(datasets, bins, cfg)
    plot_angular_resolution(datasets, bins, cfg)
