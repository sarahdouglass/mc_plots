# ── helpers ──────────────────────────────────────────────────────────────────

def pt_eta_phi(px, py, pz):
    
    """convert cartesian momentum to cylindrical
    pt  = transverse momentum, perpendicular to the beam axis z
          this is what the tracker measures via track curvature in the B field
    eta = pseudorapidity, a measure of the polar angle from the beam axis
          eta=0 is perpendicular to beam, eta=+/-inf is along the beam
          defined as -ln(tan(theta/2)) where theta is the polar angle
    phi = azimuthal angle in the transverse (x,y) plane
    """
    
    pt  = np.sqrt(px**2 + py**2)
    eta = -np.log(np.tan(0.5 * np.arctan2(pt, pz))) if pt > 0 else 0.0
    phi = np.arctan2(py, px)
    return pt, eta, phi

def delta_r(eta1, phi1, eta2, phi2):
    
    """compute the angular distance deltaR between two particles
    deltaR = sqrt(deta^2 + dphi^2) how close two particles are in angle space
    used for reco
    the dphi wrapping ensures we handle the -pi/+pi B.C.
    """
    dphi = phi1 - phi2
    dphi = (dphi + np.pi) % (2 * np.pi) - np.pi   # range [-pi, pi]
    return np.sqrt((eta1 - eta2)**2 + dphi**2)

def gaussian(x, A, mu, sigma):
    
    """standard gaussian function used for residuals
    A     = amplitude (peak height, matched to histogram bin count)
    mu    = mean (the bias of the distribution)
    sigma = standard deviation, resolution
    """
    return A * np.exp(-0.5 * ((x - mu) / sigma)**2)


