@dataclass
class EventData:
    
    """stores per-event truth and reco quantities accumulated across all files
    lists are appended to for each truth muon found. matched=True means a reco
    PFO was found within dr_cut of the truth muon. matched=False contributes
    to the efficiency denominator but not the resolution numerator
    pull quantities (d0, z0, phi, tanlam, omega) are track parameter pulls
    from the silicon tracker and are only filled if SiTracksMCTruthLink is 
    available
    """
    true_pt  : list = field(default_factory=list)
    reco_pt  : list = field(default_factory=list)
    true_eta : list = field(default_factory=list)
    reco_eta : list = field(default_factory=list)
    true_phi : list = field(default_factory=list)
    reco_phi : list = field(default_factory=list)
    matched  : list = field(default_factory=list)   # bool: was this truth muon matched?
    pull_d0    : list = field(default_factory=list)
    pull_z0    : list = field(default_factory=list)
    pull_phi   : list = field(default_factory=list)
    pull_tanlam: list = field(default_factory=list)
    pull_omega : list = field(default_factory=list)

    def arrays(self):
        
        """convert lists to numpy arrays. matched pairs are filtered using the
        boolean mask m, unmatched events are excluded from resolution/angular
        plots but kept in all_true_pt/all_matched for efficiency calculation"""
        
        m = np.array(self.matched, dtype=bool)
        return dict(
            true_pt  = np.array(self.true_pt)[m],
            reco_pt  = np.array(self.reco_pt)[m],
            true_eta = np.array(self.true_eta)[m],
            reco_eta = np.array(self.reco_eta)[m],
            true_phi = np.array(self.true_phi)[m],
            reco_phi = np.array(self.reco_phi)[m],
            all_true_pt = np.array(self.true_pt),   # includes unmatched, for efficiency denom
            all_matched = m,
            pull_d0     = np.array(self.pull_d0),
            pull_z0     = np.array(self.pull_z0),
            pull_phi    = np.array(self.pull_phi),
            pull_tanlam = np.array(self.pull_tanlam),
            pull_omega  = np.array(self.pull_omega),
        )


# ── reader ───────────────────────────────────────────────────────────────────

def process_files(file_list: list, cfg: dict) -> EventData:
    
    # LINES FOR MATCHING EDITING
    """read all LCIO files, extract truth muons and matched reco PFOs
    for each event:
      1. get truth muons from MCParticle (level 1, pT > pt_cut_mev)
      2. get reco PFOs from PandoraPFOs (filtered to tracked particles only)
      3. try MCParticlePandoraPFOs link collection for matching
      4. fall back to min-deltaR matching if link collection fails
      5. optionally fill track parameter pulls if SiTracksMCTruthLink exists
    """
    
    data   = EventData() # class
    reader = IOIMPL.LCFactory.getInstance().createLCReader()
    # https://ilcsoft.desy.de/LCIO/current/doc/doxygen_api/html/classIOIMPL_1_1LCFactory.html
   
    if cfg["max_files"]:
        file_list = file_list[:cfg["max_files"]]

    # prepend xrootd prefix if running on worker nodes without /ospool mount
    prefix = cfg.get("xrootd_prefix", "")
    if prefix:
        file_list = [prefix + f for f in file_list]

    print(f"  Processing {len(file_list)} files …")

    n_evt = 0
    for fpath in file_list:
        try:
            reader.open(fpath)
        except Exception as e:
            print(f"  WARNING: could not open {fpath}: {e}")
            continue

        while True:
            try:
                event = reader.readNextEvent() # using ILC reader
            except Exception:
                break
            if event is None:
                break
            n_evt += 1

            # print all collection names in the first event (debugging)
            # set print_collections=True in CONFIG to enable, I have it as false
            if cfg["print_collections"] and n_evt == 1:
                cols = event.getCollectionNames()
                print("collections in first event:")
                for col in cols:
                    print(f"  {col}")

            # ── get truth muons ───────────────────────────────────
            try:
                mc_col = event.getCollection(cfg["mc_col_name"]) #MCParticle
            except Exception:
                continue   # skip event if MCParticle collection missing

            truth_muons = []
            for i in range(mc_col.getNumberOfElements()):
                mc = mc_col.getElementAt(i)
                # generator status 1 = primary stable particle from the gun
                # status 2 = decayed, status 3 = documentation
                # abs(PDG) gets both mu- and mu+
                if abs(mc.getPDG()) == cfg["muon_pdg"] and mc.getGeneratorStatus() == 1:
                    p = mc.getMomentum()
                    pt, eta, phi = pt_eta_phi(p[0], p[1], p[2])
                    # apply 2 GeV pT cut
                    if pt < cfg.get("pt_cut_mev", 0):
                        continue
                    truth_muons.append(dict(mc=mc, pt=pt, eta=eta, phi=phi))

            # ── get reco PFOs ─────────────────────────────────────
            try:
                pfo_col = event.getCollection(cfg["pfo_col_name"]) #PandoraPFOs
            except Exception:
                # if PandoraPFOs is missing, mark all truth muons as unmatched
                for tm in truth_muons:
                    data.true_pt.append(tm["pt"])
                    data.true_eta.append(tm["eta"])
                    data.true_phi.append(tm["phi"])
                    data.reco_pt.append(np.nan)
                    data.reco_eta.append(np.nan)
                    data.reco_phi.append(np.nan)
                    data.matched.append(False)
                continue

            pfos = []
            for i in range(pfo_col.getNumberOfElements()):
                pfo = pfo_col.getElementAt(i)

                # key BIB filter: require at least one associated track
                # muons are charged and always produce a track in the silicon tracker
                # BIB neutrals (neutrons type=2112, photons type=22) have nTracks=0
                # and were responsible for the -100% spike in the residual plot
                
                if pfo.getTracks().size() == 0: # this may be redundant with pdg condition in place
                    continue

                # additionally require the PFO PDG type matches the muon PDG
                # this filters BIB pions (type=-211) and electrons (type=11)
                # that happen to have a track but are not muons
                # we saw these in the diagnostic: type=-211 with res~7% being
                # incorrectly matched to truth muons
                
                if abs(pfo.getType()) != cfg["muon_pdg"]:
                    continue

                p = pfo.getMomentum()
                pt, eta, phi = pt_eta_phi(p[0], p[1], p[2])
                pfos.append(dict(pfo=pfo, pt=pt, eta=eta, phi=phi))

            # ── try link collection for matching ──────────────────
            # MCParticlePandoraPFOs is an LCRelation collection associating
            # MCParticles to PFOs with a weight. higher weight = more of the
            # PFO energy came from that MC particle
            # in practice for the muon gun this often fails (navigator returns
            # 0 relations) so we fall back to deltaR matching
            
            link_map = {}   # maps pfo index → mc object
            if cfg["link_col_name"]:
                try:
                    link_col = event.getCollection(cfg["link_col_name"])
                    nav = UTIL.LCRelationNavigator(link_col)
                    for i, pd in enumerate(pfos):
                        rels = nav.getRelatedToObjects(pd["pfo"])
                        wts  = nav.getRelatedToWeights(pd["pfo"])
                        if rels.size() > 0:
                            # take the MC particle with the highest weight
                            best_idx = int(np.argmax([wts.elementAt(j) for j in range(wts.size())]))
                            link_map[i] = rels.elementAt(best_idx)
                except Exception:
                    pass   # link collection unavailable — fall through to deltaR

            # ── match each truth muon to a reco PFO ──────────────
            # once a PFO is used, it can't be matched again
            # first tries link collection (exact), then falls back to min-deltaR
            used_pfo = set()
            for tm in truth_muons:
                data.true_pt.append(tm["pt"])
                data.true_eta.append(tm["eta"])
                data.true_phi.append(tm["phi"])

                matched_pfo = None

                # try link collection first
                for pi, pd in enumerate(pfos):
                    if pi in used_pfo:
                        continue
                    if pi in link_map and link_map[pi] is tm["mc"]:
                        matched_pfo = (pi, pd)
                        break

                # fall back to min-deltaR if link collection gave nothing
                if matched_pfo is None:
                    best_dr, best_pi, best_pd = cfg["dr_cut"], None, None
                    for pi, pd in enumerate(pfos):
                        if pi in used_pfo:
                            continue
                        dr = delta_r(tm["eta"], tm["phi"], pd["eta"], pd["phi"])
                        if dr < best_dr:
                            best_dr, best_pi, best_pd = dr, pi, pd
                    if best_pi is not None:
                        matched_pfo = (best_pi, best_pd)

                if matched_pfo is not None:
                    pi, pd = matched_pfo
                    used_pfo.add(pi)
                    data.reco_pt.append(pd["pt"])
                    data.reco_eta.append(pd["eta"])
                    data.reco_phi.append(pd["phi"])
                    data.matched.append(True)
                else:
                    # no match found within dr_cut — counts as inefficiency
                    data.reco_pt.append(np.nan)
                    data.reco_eta.append(np.nan)
                    data.reco_phi.append(np.nan)
                    data.matched.append(False)

            # ──track parameter pulls (optional) ──────────────────
            # pulls measure how well-calibrated the track fit uncertainties are
            # a pull = (measured - truth) / uncertainty should be a unit gaussian
            # if the errors are correct. only filled if SiTracksMCTruthLink exists
            # track parameters at the IP (interaction point):
            #   d0     = transverse impact parameter (closest approach in xy plane)
            #   z0     = longitudinal impact parameter (closest approach along z)
            #   phi    = azimuthal angle of track at closest approach
            #   tanlam = tan(lambda) where lambda is the dip angle from xy plane
            #   omega  = signed curvature = q/R (sign gives charge, magnitude gives pT)
            
            try:
                trk_col = event.getCollection(cfg["track_col_name"])
                mc_trk_nav = None
                try:
                    # SiTracksMCTruthLink links silicon tracks to MC particles
                    mc_trk_col = event.getCollection("SiTracksMCTruthLink")
                    mc_trk_nav = UTIL.LCRelationNavigator(mc_trk_col)
                except Exception:
                    pass   # link not available — pulls will be empty

                for i in range(trk_col.getNumberOfElements()):
                    trk   = trk_col.getElementAt(i)
                    state = trk.getTrackState(EVENT.TrackState.AtIP)
                    if state is None:
                        continue

                    # covariance matrix is lower-triangular, packed as a flat array.
                    # diagonal elements give the variance of each parameter:
                    # cov[0]=var(d0), cov[2]=var(phi), cov[5]=var(omega),
                    # cov[9]=var(z0), cov[14]=var(tanlambda)
                    cov = state.getCovMatrix()
                    sig_d0  = np.sqrt(max(cov[0],  0))
                    sig_phi = np.sqrt(max(cov[2],  0))
                    sig_omg = np.sqrt(max(cov[5],  0))
                    sig_z0  = np.sqrt(max(cov[9],  0))
                    sig_tan = np.sqrt(max(cov[14], 0))

                    if mc_trk_nav:
                        mcs = mc_trk_nav.getRelatedToObjects(trk)
                        if mcs.size() == 0:
                            continue
                        mc_p = mcs.elementAt(0)
                        p    = mc_p.getMomentum()
                        pt_mc, _, phi_mc = pt_eta_phi(p[0], p[1], p[2])
                        pz_mc = p[2]
                        # d0 and z0 truth is 0 for particles from the IP
                        if sig_d0  > 0: data.pull_d0.append(state.getD0()  / sig_d0)
                        if sig_phi > 0: data.pull_phi.append((state.getPhi() - phi_mc) / sig_phi)
                        if sig_z0  > 0: data.pull_z0.append(state.getZ0()  / sig_z0)
                        if sig_tan > 0:
                            tan_mc = pz_mc / pt_mc if pt_mc > 0 else 0
                            data.pull_tanlam.append((state.getTanLambda() - tan_mc) / sig_tan)
                        if sig_omg > 0: data.pull_omega.append(state.getOmega() / sig_omg)
            except Exception:
                pass   # track pulls are optional — skip silently if unavailable

        reader.close()

    print(f"  Processed {n_evt} events.")
    return data


