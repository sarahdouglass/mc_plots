def gauss_fit(values, n_bins=60):
    
    """fit a gaussian to a 1d distribution. returns (mu, sigma, fit_ok)
    fits over a range of +-4 sigma around the mean to exclude far outliers
    uses scipy curve_fit with bounds to prevent unphysical solutions
    falls back to raw mean/std if the fit fails
    """
    if len(values) < 10:
        return np.nan, np.nan, False
    mu0, sig0 = np.mean(values), np.std(values)
    counts, edges = np.histogram(values, bins=n_bins,
                                 range=(mu0 - 4*sig0, mu0 + 4*sig0))
    centers = 0.5 * (edges[:-1] + edges[1:])
    try:
        p0 = [counts.max(), mu0, sig0]
        popt, _ = curve_fit(gaussian, centers, counts, p0=p0,
                            maxfev=5000,
                            bounds=([0, mu0-3*sig0, 0],
                                    [counts.max()*2, mu0+3*sig0, 3*sig0]))
        return popt[1], abs(popt[2]), True
    except Exception:
        return mu0, sig0, False   # return raw stats if fit fails


def plot_pt_resolution(datasets: dict, bins, cfg):
    
    """plot pT resolution (sigma) and bias (mean) vs true pT
    for each pT bin, fits a gaussian to the residual distribution
    (reco_pT - true_pT) / true_pT and extracts mu (bias) and sigma (resolution)
    error bars on resolution use the standard result: err(sigma) = sigma / sqrt(2N)
    """
    fig, axes = plt.subplots(1, 2, figsize=(13, 5))

    for tag, arr in datasets.items():
        st  = STYLE[tag]
        # fractional residual (not in percent !! need mult 100)
        res = (arr["reco_pt"] - arr["true_pt"]) / arr["true_pt"]
        centers, sigmas, sig_errs, means = [], [], [], []

        for lo, hi in zip(bins[:-1], bins[1:]):
            mask = (arr["true_pt"] >= lo) & (arr["true_pt"] < hi)
            if mask.sum() < cfg["min_events_per_bin"]:
                continue   # skip bins with too few events, bad fit otherwise
            mu, sig, ok = gauss_fit(res[mask])
            centers.append(0.5 * (lo + hi))
            sigmas.append(sig * 100)         # convert to percent
            means.append(mu * 100)
            sig_errs.append(sig / np.sqrt(2 * mask.sum()) * 100)   # err(sigma) = sigma/sqrt(2N)

        centers  = np.array(centers)
        sigmas   = np.array(sigmas)
        sig_errs = np.array(sig_errs)
        means    = np.array(means)

        # left panel resolution (sigma) vs pT
        axes[0].errorbar(centers, sigmas, yerr=sig_errs,
                         fmt=f"{st['marker']}-", color=st["color"],
                         label=st["label"], capsize=4, linewidth=1.5)
        # right panel bias (mean) vs pT
        axes[1].plot(centers, means, f"{st['marker']}-",
                     color=st["color"], label=st["label"], linewidth=1.5)

    for ax, ylabel, title in zip(
        axes,
        [r"$\sigma(\Delta p_T/p_T)$ [%]", r"Mean $\Delta p_T/p_T$ [%]"],
        [r"Muon $p_T$ Resolution", r"$p_T$ Bias"]
    ):
        ax.set_xlabel(r"True $p_T$ [MeV]", fontsize=12)
        ax.set_ylabel(ylabel, fontsize=12)
        ax.set_title(title, fontsize=13)
        ax.legend(fontsize=11)
        ax.grid(alpha=0.3)
        ax.set_xlim(bins[0], bins[-1])

    fig.suptitle("Muon $p_T$ Resolution: BIB Comparison", fontsize=14, fontweight="bold")
    fig.tight_layout()
    _save(fig, cfg["out_dir"], "01_pt_resolution.png", cfg["dpi"])


def plot_efficiency(datasets: dict, bins, cfg):
    
    """plot reconstruction efficiency vs true pT
    efficiency = N(matched reco) / N(truth muons) per pT bin
    error bars use the wilson score interval which is more accurate than
    the naive binomial error, especially near 0% or 100%
    """
    fig, ax = plt.subplots(figsize=(7, 5))

    for tag, arr in datasets.items():
        st = STYLE[tag]
        effs, errs, centers = [], [], []

        for lo, hi in zip(bins[:-1], bins[1:]):
            # use all_true_pt (includes unmatched) for the denominator
            mask  = (arr["all_true_pt"] >= lo) & (arr["all_true_pt"] < hi)
            n_tot = mask.sum()
            if n_tot < cfg["min_events_per_bin"]:
                continue
            n_match = arr["all_matched"][mask].sum()
            eff = n_match / n_tot
            # WHAT IS IT DOING HERE?? WHAT IS A WILSON SCORE?
            # wilson score interval: more reliable than sqrt(p(1-p)/n) near 0 or 1
            z     = 1.96   # 95% confidence level
            denom = 1 + z**2 / n_tot
            p_hat = (eff + z**2 / (2 * n_tot)) / denom
            err   = z * np.sqrt(eff * (1 - eff) / n_tot + z**2 / (4 * n_tot**2)) / denom
            effs.append(p_hat * 100)
            errs.append(err * 100)
            centers.append(0.5 * (lo + hi))

        ax.errorbar(centers, effs, yerr=errs,
                    fmt=f"{st['marker']}-", color=st["color"],
                    label=st["label"], capsize=4, linewidth=1.5)

    ax.set_xlabel(r"True $p_T$ [MeV]", fontsize=12)
    ax.set_ylabel("Reconstruction Efficiency [%]", fontsize=12)
    ax.set_title("Muon Reconstruction Efficiency", fontsize=13)
    ax.set_ylim(0, 105)
    ax.axhline(100, color="gray", linestyle="--", linewidth=0.8)   # ideal 100% line
    ax.legend(fontsize=11)
    ax.grid(alpha=0.3)
    ax.set_xlim(bins[0], bins[-1])
    fig.tight_layout()
    _save(fig, cfg["out_dir"], "02_efficiency.png", cfg["dpi"])


def plot_angular_resolution(datasets: dict, bins, cfg):
    
    """plot angular resolution (sigma of delta_eta and delta_phi) vs true pT
    angular resolution measures how well the direction of the muon is reconstructed,
    independent of the momentum magnitude. for the muon collider with BIB, this is
    expected to be better preserved than pT resolution since BIB adds random hits
    that bias curvature (pT) more than direction
    """
    fig, axes = plt.subplots(1, 2, figsize=(13, 5))

    for tag, arr in datasets.items():
        st    = STYLE[tag]
        d_eta = arr["reco_eta"] - arr["true_eta"]
        d_phi = arr["reco_phi"] - arr["true_phi"]
        # dphi [-pi, pi] to handle boundary crossing
        d_phi = (d_phi + np.pi) % (2 * np.pi) - np.pi

        for ax, residual, ylabel in zip(
            axes,
            [d_eta, d_phi],
            [r"$\sigma(\Delta\eta)$", r"$\sigma(\Delta\phi)$ [rad]"],
        ):
            centers, sigmas, sig_errs = [], [], []
            for lo, hi in zip(bins[:-1], bins[1:]):
                mask = (arr["true_pt"] >= lo) & (arr["true_pt"] < hi)
