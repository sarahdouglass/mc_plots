def plot_residual_distributions(datasets: dict, cfg):
    
    """plot pT residual distributions: full range and zoomed core peak
    left panel: full range histogram showing the complete residual distribution
    including BIB failure events. the spike near -100% (now removed by the
    track + PDG filters) would appear here if matching were poor
    
    right panel: zoomed to +-30% with a gaussian fit to the core peak
    the fit captures well-reconstructed muons only. counts.max() is used
    to anchor the gaussian amplitude to the tallest histogram bar
    mu = bias (should be near 0%), sigma = core resolution
    """
    fig, axes = plt.subplots(1, 2, figsize=(12, 5))

    for tag, arr in datasets.items():
        st  = STYLE[tag]
        res = (arr["reco_pt"] - arr["true_pt"]) / arr["true_pt"] * 100

        # left panel full range to show any BIB failure tails
        axes[0].hist(res, bins=100, color=st["color"], alpha=0.7,
                     edgecolor="k", linewidth=0.3)
        axes[0].set_xlabel(r"$(p_T^{\rm reco} - p_T^{\rm true})/p_T^{\rm true}$ [%]", fontsize=11)
        axes[0].set_ylabel("Events", fontsize=11)
        axes[0].set_title("Full Range (BIB failures visible)", fontsize=12)
        axes[0].grid(alpha=0.3)

        # right panel core peak only — excludes large residuals from BIB
        core   = res[(res > -30) & (res < 30)]
        n_bins = 60
        counts, edges, _ = axes[1].hist(core, bins=n_bins, range=(-30, 30),
                                         color=st["color"], alpha=0.7,
                                         edgecolor="k", linewidth=0.3,
                                         label="Core events")
        if len(core) > 10:
            mu, sig, ok = gauss_fit(core)
            if ok:
                x = np.linspace(-30, 30, 300)
                # anchor gaussian peak to the tallest bin in THIS histogram
                axes[1].plot(x, gaussian(x, counts.max(), mu, sig),
                             "k-", linewidth=2,
                             label=rf"$\mu={mu:.2f}\%$, $\sigma={sig:.2f}\%$")
        axes[1].set_xlabel(r"$(p_T^{\rm reco} - p_T^{\rm true})/p_T^{\rm true}$ [%]", fontsize=11)
        axes[1].set_ylabel("Events", fontsize=11)
        axes[1].set_title("Core Peak (well-reconstructed muons)", fontsize=12)
        axes[1].legend(fontsize=10)
        axes[1].grid(alpha=0.3)

    fig.suptitle(f"$p_T$ Residual — {STYLE[list(datasets.keys())[0]]['label']}",
                 fontsize=13, fontweight="bold")
    fig.tight_layout()
    _save(fig, cfg["out_dir"], "00_residual_distributions.png", cfg["dpi"])


def _save(fig, out_dir, fname, dpi):
    """save figure to disk and close it to free memory."""
    os.makedirs(out_dir, exist_ok=True)
    path = os.path.join(out_dir, fname)
    fig.savefig(path, dpi=dpi, bbox_inches="tight")
    print(f"  Saved → {path}")
    plt.close(fig)


